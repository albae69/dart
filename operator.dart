main(List<String> args) {
  // Aritmatika
  int a = 4, b = 10;

  // penjumlahan
  int penjumlahan = a + b;
  // pengurangan
  int pengurangan = a - b;
  // perkalian
  int perkalian = a * b;
  // pembagian
  num pembagian = a / b;
  // modulus / sisa dari hasil bagi
  int modulus = a % b;

  print(penjumlahan);
  print(pengurangan);
  print(perkalian);
  print(pembagian);
  print(modulus);

  // Persamaan dan Relasi
  // lebih dari
  print(a > b);
  // kurang dari
  print(a < b);
  // sama dengan
  print(a == b);
  // tidak sama dengan
  print(a != b);
  // lebih besar dari sama dengan
  print(a >= b);
  // lebih kecil dari sama dengan
  print(a <= b);

  // Logika
  bool x = true;
  bool y = false;
  print(x && y);
  print(x || y);
  print(!x);
}
